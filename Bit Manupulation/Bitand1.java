public class Bitand1 {
    public static void main(String[] args) {
        System.out.println(5&6); // & is bitwise operator and && is for checking AND condition and it is logical operator
        System.out.println(5|6);//OR
        System.out.println(5^6);//XOR
        System.out.println(~5); // Binary one's complement/ Not operator
        System.out.println(~0); // will give negative value
        System.out.println(5<<2); //Binary left shift formula= a*2^b
        System.out.println(6>>1);// Binary Right shift formula= a/2^b
   
    }
    
}
